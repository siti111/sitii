    private void ambilData() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No");
        tbl.addColumn("ID");
        tbl.addColumn("Judul");
        tbl.addColumn("Tipe");
        tbl.addColumn("Episode");
        tbl.addColumn("Genre");
        tbl.addColumn("Status");
        tbl.addColumn("Rating");
        table_anime.setModel(tbl);
        try{
            Statement statement = (Statement) koneksi.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select * from data_anime");
            int index = 1;
            while (res.next())
            {
                tbl.addRow(new Object []{
                    index,
                    res.getString("id"),
                    res.getString("judul"),
                    res.getString("tipe"),
                    res.getString("episode"),
                    res.getString("genre"),
                    res.getString("status"),
                    res.getString("rating"),
                });
                table_anime.setModel(tbl);
                index++;
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal Mengambil Data: " + e.getMessage());
        }
    }
    
