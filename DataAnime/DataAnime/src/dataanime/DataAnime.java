    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
          if(!txtId.getText().equals("")){
            JOptionPane.showMessageDialog(null, 
                    "Silahkan refresh terlebih dahulu!");
        }
        else{
            String judul = txtJudul.getText();
            String tipe = txtTipe.getText();
            String episode = txtEpisode.getText();
            String genre = txtGenre.getText();
            String status = (String) cmbStatus.getSelectedItem();
            String rating = txtRating.getText();

            try{
                Statement statement = (Statement) koneksi.getConnection().createStatement();
                statement.executeUpdate("insert into data_anime VALUES (null, '" + judul + "','" 
                            + tipe +"','"+ episode +"','"+ genre +"','"+ status +"','"+ rating +"')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");

                refresh();
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(txtId.getText().equals("")){
            JOptionPane.showMessageDialog(null, 
                    "Silahkan pilih terlebih dahulu data film yang akan dihapus!");
        }
        else{
            String id = txtId.getText();
            
            try{
                Statement statement = (Statement) koneksi.getConnection().createStatement();
                statement.executeUpdate("delete from data_anime where id = '" + id + "'");
                JOptionPane.showMessageDialog(null, "Data Berhasil di Hapus");
                
                refresh();
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Data Gagal di Hapus: " + e.getMessage());
            }
        }        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if(txtId.getText().equals("")){
            JOptionPane.showMessageDialog(null, 
                    "Silahkan pilih terlebih dahulu data film yang akan  diupdate!");
        }
        else{
            String id = txtId.getText();
            String judul = txtJudul.getText();
            String tipe = txtTipe.getText();
            String episode = txtEpisode.getText();
            String genre = txtGenre.getText();
            String status = (String) cmbStatus.getSelectedItem();
            String rating = txtRating.getText();

            try{
                Statement statement = (Statement) koneksi.getConnection().createStatement();
                statement.executeUpdate("update data_anime SET judul='"+judul+"'," + "tipe='"+tipe+"',"
                        + "episode='"+episode+"'," + "genre='"+genre+"'," + "status='"+status+"',"
                                + "rating='"+rating+"' " + "WHERE id = '"+id+"'");
                statement.close();
                JOptionPane.showMessageDialog(null, "Data Berhasil diubah");

                refresh();
            }catch (Exception e){
                JOptionPane.showMessageDialog(null, "Data Gagal diubah: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

